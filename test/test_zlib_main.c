unsigned long clr_t[] = {  // BGRA order
	0x00ffff,  // 0
	0x00edff,  // 1
	0x00c1ff,  // 2
	0x0099ff,  // 3
	0x0066ff,  // 4
	0x003bff,  // 5
	0x000fff,  // 6
	0x1500e2,  // 7
	0x3400b7,  // 8
	0x54008b,  // 9
	0x730060,  // a
	0x990033,  // b
	0xb20009   // c
};

void showpng(unsigned char *data)
{
	int i = 0;

	for(i=0; i<40; i+=5)
	{
		int j = i+1;
		for(; j < i+5; j++)
		{
			int index = data[j];
			int h = clr_t[index>>4], l = clr_t[index&0xf];
			putpixel_big(j/5, (j%5-1)*2, h, 32);
			putpixel_big(j/5, (j%5-1)*2+1, l, 32);
		}
	}
}

